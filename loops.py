# While loop
# Incrementing
number = 20
while number <= 29:
    print(number)
    number += 1

# Decrementing
num = 105
while num >= 100:
    print(num)
    num -= 1

# Break statement
x = 30
while x <= 40:
    print(x)
    if x == 36:
        break
    x += 1

# Continue statement
y = 29
while y < 35:
    y += 1
    if y == 32:
        continue
    print(y)

# For loop
languages = [ "Python","Java","c++"]
for lang in languages:
    print(lang)

